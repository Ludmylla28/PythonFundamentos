## Calculos basicos
#Soma 
print(4+5)

#Subtração 
print(9-3)

#Mulriplicação 
print(5*3)

#Divisão 
print(9/2)

#Potencia
print(4**2)

#Modulo 
print (10%3)

##Type

print(type(5))
print(type(6.3))

a = "Sou a mais linda do mundo"
